              <p>There is something truly unique about the Friends of Westwood Playground. The Westwood neighborhood itself is over 150 years old founded by freed slaves and located on land formerly in Henrico ​County. At the heart of the neighborhood is Westwood Baptist Church, which is where we met this week to discuss the organization and to get a tour of the Westwood Playground.</p>
              <p>We ​met with the President, Vice President and the Treasurer in one of the halls at the large historic church. The walls of the church are lined with beautiful paintings, some made by past members. Upstairs is the original sanctuary, a layer of carpet hides the old <a href="http://enrichmond.org/phpages/wp-content/uploads/2016/04/westwood2.jpg"><img class="size-thumbnail wp-image-2857 alignright" alt="westwood2" src="http://enrichmond.org/phpages/wp-content/uploads/2016/04/westwood2-150x150.jpg" width="150" height="150" srcset="http://enrichmond.org/phpages/wp-content/uploads/2016/04/westwood2-150x150.jpg 150w, http://enrichmond.org/phpages/wp-content/uploads/2016/04/westwood2-152x152.jpg 152w" sizes="(max-width: 150px) 100vw, 150px" /></a>flooring, a few small cracks lay in the original stained glass, looking​  up ​you ​see wonderful murals and an authentic tiled ceiling. Both Jeanette and Tammy tell ​us things like “I was baptized here” or “I got married here&#8221;.  This is a peaceful, rejuvenating place, a piece of history that a lot of Richmond has never heard.</p>
              <p>We walk outside and into the old Westwood Baptist ​Church ​Cemetery and down Marian Street towards the large playground. The rich stories continue to bound off the houses like the faint sound of Patterson Avenue in the distance. As we walk towards the site ​we a​re told ​the playground is located on the site of the old one-room Westwood schoolhouse.</p>
              <p>The playground is surrounded by houses, by neighbors&#8211;by friends. All the members at Friends of Westwood Playground tell ​us when they were growing up the playground was the place to be a​after school. You would stay until the street lights came on and you were called home.</p>
              <p>Life​ was simpler then. Our friends at the Westwood Playground have a simple mission: to bring this playground back to its former glory. They want to showcase the playground as an important part of the neighborhood, to hold events there, and to come together in order to make sure it is a safe place for all.</p>
              <p>Currently, the Westwood Playground is being spruced up. You may have noticed the building and awnings have been painted! Electricity is now on, they have new picnic tables and benches​, and sand is being dredged. Further, Chesapeake Bank has made considerable donations, both of money and of time, equipment, food, and supplies.</p>
              <p>On Saturday,​ April 23rd from 10:30am-4:30pm ​our friends are having a cleanup and flower planting at the playground. Food + drink are being supplied by Chesapeake Bank. Tools are being supplied by the ToolBank and Friends of Westwood playground. Come by and say hi to all of our hardworking friends at Westwood playground.</p>
              <p>If you are ever in the neighborhood, stop by Westwood playground say hello, have a seat, and let the history surround you.</p>
